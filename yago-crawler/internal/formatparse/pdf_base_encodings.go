package formatparse

func pdfBaseEncodingTable(name string) map[uint32]string {
	table := map[uint32]string{}
	switch name {
	case "WinAnsiEncoding", "MacRomanEncoding", "StandardEncoding":
		for code := byte(0x20); code <= 0x7E; code++ {
			table[uint32(code)] = string(rune(code))
		}
	}
	switch name {
	case "WinAnsiEncoding":
		for code := uint32(0xA0); code <= 0xFF; code++ {
			table[code] = string(rune(code))
		}
		for code, text := range pdfWinAnsiExtensions {
			table[code] = text
		}
	case "MacRomanEncoding":
		for code, text := range pdfMacRomanEncodingCharacters {
			table[code] = text
		}
	case "StandardEncoding":
		for code, text := range pdfStandardEncodingCharacters {
			table[code] = text
		}
	}

	return table
}

